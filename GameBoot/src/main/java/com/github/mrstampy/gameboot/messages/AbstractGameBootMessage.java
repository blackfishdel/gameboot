package com.github.mrstampy.gameboot.messages;

import org.apache.commons.lang3.builder.ToStringBuilder;

import com.fasterxml.jackson.annotation.JsonInclude;
import com.fasterxml.jackson.annotation.JsonInclude.Include;

// TODO: Auto-generated Javadoc
/**
 * The Class AbstractGameBootMessage.
 */
@JsonInclude(Include.NON_NULL)
public abstract class AbstractGameBootMessage {

	private Integer id;

	private MessageType type;

	/**
	 * Instantiates a new abstract game boot message.
	 *
	 * @param type
	 *          the type
	 */
	protected AbstractGameBootMessage(MessageType type) {
		setType(type);
	}

	/**
	 * Gets the id.
	 *
	 * @return the id
	 */
	public Integer getId() {
		return id;
	}

	/**
	 * Sets the id.
	 *
	 * @param id
	 *          the new id
	 */
	public void setId(Integer id) {
		this.id = id;
	}

	/**
	 * Gets the type.
	 *
	 * @return the type
	 */
	public MessageType getType() {
		return type;
	}

	/**
	 * Sets the type.
	 *
	 * @param type
	 *          the new type
	 */
	public void setType(MessageType type) {
		this.type = type;
	}

	/*
	 * (non-Javadoc)
	 * 
	 * @see java.lang.Object#toString()
	 */
	@Override
	public String toString() {
		return ToStringBuilder.reflectionToString(this);
	}

}
