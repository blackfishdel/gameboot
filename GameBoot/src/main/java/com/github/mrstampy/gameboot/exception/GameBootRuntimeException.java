package com.github.mrstampy.gameboot.exception;

import com.github.mrstampy.gameboot.messages.context.ResponseContext;

/**
 * Runtime exception generated by GameBoot.
 */
public class GameBootRuntimeException extends RuntimeException implements GameBootThrowable {

  private static final long serialVersionUID = -439786992079674082L;

  private Object[] payload;

  private ResponseContext error;

  private Integer errorCode;

  /**
   * Instantiates a new game boot runtime exception.
   *
   * @param error
   *          the error
   * @param payload
   *          the payload
   */
  public GameBootRuntimeException(ResponseContext error, Object... payload) {
    super();
    setPayload(payload);
    setError(error);
  }

  /**
   * Instantiates a new game boot runtime exception.
   *
   * @param message
   *          the message
   * @param error
   *          the error
   * @param payload
   *          the payload
   */
  public GameBootRuntimeException(String message, ResponseContext error, Object... payload) {
    super(message);
    setPayload(payload);
    setError(error);
  }

  /**
   * Instantiates a new game boot runtime exception.
   *
   * @param errorCode
   *          the error code
   * @param payload
   *          the payload
   */
  public GameBootRuntimeException(Integer errorCode, Object... payload) {
    super();
    setErrorCode(errorCode);
    setPayload(payload);
  }

  /**
   * Instantiates a new game boot runtime exception.
   *
   * @param message
   *          the message
   * @param errorCode
   *          the error code
   * @param payload
   *          the payload
   */
  public GameBootRuntimeException(String message, Integer errorCode, Object... payload) {
    super(message);
    setErrorCode(errorCode);
    setPayload(payload);
  }

  /*
   * (non-Javadoc)
   * 
   * @see com.github.mrstampy.gameboot.exception.GameBootThrowable#hasPayload()
   */
  public boolean hasPayload() {
    return payload != null && payload.length > 0;
  }

  /*
   * (non-Javadoc)
   * 
   * @see com.github.mrstampy.gameboot.exception.GameBootThrowable#getPayload()
   */
  public Object[] getPayload() {
    return payload;
  }

  /*
   * (non-Javadoc)
   * 
   * @see
   * com.github.mrstampy.gameboot.exception.GameBootThrowable#setPayload(java.
   * lang.Object[])
   */
  public void setPayload(Object[] payload) {
    if (payload != null && payload.length == 0) payload = null;
    this.payload = payload;
  }

  /*
   * (non-Javadoc)
   * 
   * @see com.github.mrstampy.gameboot.exception.GameBootThrowable#getError()
   */
  public ResponseContext getError() {
    return error;
  }

  /*
   * (non-Javadoc)
   * 
   * @see com.github.mrstampy.gameboot.exception.GameBootThrowable#setError(com.
   * github.mrstampy.gameboot.messages.error.Error)
   */
  public void setError(ResponseContext error) {
    this.error = error;
  }

  /*
   * (non-Javadoc)
   * 
   * @see
   * com.github.mrstampy.gameboot.exception.GameBootThrowable#getErrorCode()
   */
  public Integer getErrorCode() {
    return errorCode;
  }

  /*
   * (non-Javadoc)
   * 
   * @see
   * com.github.mrstampy.gameboot.exception.GameBootThrowable#setErrorCode(java.
   * lang.Integer)
   */
  public void setErrorCode(Integer errorCode) {
    this.errorCode = errorCode;
  }

}
